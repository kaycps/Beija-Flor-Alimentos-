package Classes;

public class forcenedor {
	
	private String nome;
	private String telefone;
	private String cnpj;	
	private String produtoFornceido;
	private int id;
	private endereco endereco;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	
	public String getProdutoFornceido() {
		return produtoFornceido;
	}
	public void setProdutoFornceido(String produtoFornceido) {
		this.produtoFornceido = produtoFornceido;
	}
	public endereco getEndereco() {
		return endereco;
	}
	public void setEndereco(endereco endereco) {
		this.endereco = endereco;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
	
}
